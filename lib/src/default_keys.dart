import 'game_screen.dart';

/// A set of default keys for use with [GameScreen]s.
class DefaultKeys {
  /// No constructor.
  DefaultKeys._();

  /// The key used to quit a game.
  static const String quitGame = 'q';

  /// The key to use to move the cursor up.
  static const String cursorUp = 'w';

  /// The key to use to move the cursor down.
  static const String cursorDown = 's';

  /// The key to use to move the cursor left.
  static const String cursorLeft = 'a';

  /// The key to use to move the cursor right.
  static const String cursorRight = 'd';
}
